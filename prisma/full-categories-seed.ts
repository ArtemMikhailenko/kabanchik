async function clearExistingCategoriesAndSubcategories() {
  console.log('üóëÔ∏è Clearing existing categories and subcategories...')
  
  // Delete all orders first (due to foreign key constraints)
  await prisma.order.deleteMany()
  console.log('üóëÔ∏è Deleted all orders')
  
  // Delete all categories (this will also delete subcategories due to cascade)
  await prisma.category.deleteMany()
  console.log('üóëÔ∏è Deleted all categories')
}

export async function seedCategoriesWithSubcategories() {
  console.log('üå± Seeding categories with subcategories...')
  
  // Clear existing data first
  await clearExistingCategoriesAndSubcategories()
  
  // Create parent categories and their subcategories
  for (let i = 0; i < categoriesWithSubcategories.length; i++) {
    const categoryData = categoriesWithSubcategories[i]
    
    // Create parent category
    const parentCategory = await prisma.category.create({
      data: {
        name: categoryData.name,
        slug: categoryData.slug,
        isActive: true
      }
    })
    
    console.log(`‚úÖ Created parent category: ${parentCategory.name}`)
    
    // Create subcategories
    for (let j = 0; j < categoryData.subcategories.length; j++) {
      const subcategoryName = categoryData.subcategories[j]
      const subcategorySlug = `${categoryData.slug}-${subcategoryName.toLowerCase()
        .replace(/\s+/g, '-')
        .replace(/[^a-z0-9-–∞-—è]/gi, '')
        .replace(/--+/g, '-')
        .replace(/^-|-$/g, '')}`
      
      const subcategory = await prisma.category.create({
        data: {
          name: subcategoryName,
          slug: subcategorySlug,
          isActive: true
        }
      })
      
      console.log(`  üìÅ Created subcategory: ${subcategory.name}`)
    }
  }
  
  console.log('‚ú® Categories with subcategories seeded successfully!')
  console.log(`üìä Total: ${categoriesWithSubcategories.length} parent categories created`)
  
  const totalSubcategories = categoriesWithSubcategories.reduce((sum, cat) => sum + cat.subcategories.length, 0)
  console.log(`üìä Total: ${totalSubcategories} subcategories created`)
}

